class GoogleMapScreen extends StatefulWidget {
  @override
  _GoogleMapScreenState createState() => _GoogleMapScreenState();
}

class _GoogleMapScreenState extends State<GoogleMapScreen> {
  Set<Marker> _markers = {};
  late BitmapDescriptor mapMarker;

  @override
  void initState() {
    super.initState();
    setCustomMarker();
  }

  void setCustomMarker() async {
    mapMarker = await BitmapDescriptor.fromAssetImage(
        ImageConfiguration(), 'assets/destination_map_marker.png');
  }

  void _onMapCreated(GoogleMapController controller) {
    controller.setMapStyle(Utils.mapStyle);
    setState(() {
      _markers.add(
        Marker(
          markerId: MarkerId('TabasoomGarage'),
          position: LatLng(23.7729, 90.4059),
          icon: mapMarker,
          infoWindow: InfoWindow(
            title: 'Tabasoom Garage',
            snippet: 'Parking Spot',
            onTap: () {
              Navigator.push(context,
                  MaterialPageRoute(builder: (context) => VideoExample()));
            },
          ),
        ),
      );
    });
  }

  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('MAP'),
      ),
      body: GoogleMap(
        onMapCreated: _onMapCreated,
        markers: _markers,
        initialCameraPosition: CameraPosition(
          target: LatLng(23.7729, 90.4059),
          zoom: 15,
        ),
      ),
    );
  }
}

